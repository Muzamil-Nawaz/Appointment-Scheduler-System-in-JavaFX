package appointmentscheduler;

/**
 * This class represents the Appointment details, and works as a model class
 *
 * @author
 */
public class Appointment{

    int id;
    String title;
    String description;
    String location;
    String contact;
    String type;
    String startDateTime;
    String endDateTime;
    int customerId;
    int userId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStartDateTime() {
        return startDateTime;
    }

    public void setStartDateTime(String startDateTime) {
        this.startDateTime = startDateTime;
    }

    public String getEndDateTime() {
        return endDateTime;
    }

    public void setEndDateTime(String endDateTime) {
        this.endDateTime = endDateTime;
    }

    public int getCustomerId() {
        return customerId;
    }

    public void setCustomerId(int customerId) {
        this.customerId = customerId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    @Override
    public String toString() {
        return "Appointment{" + "id=" + id + ", title=" + title + ", description=" + description + ", location=" + location + ", contact=" + contact + ", type=" + type + ", startDateTime=" + startDateTime + ", endDateTime=" + endDateTime + ", customerId=" + customerId + ", userId=" + userId + '}';
    }

}
